export const iconify = (icon) => {
    switch (icon) {
        case 'food':
            return 'material-symbols:food-bank-outline'
        case 'coffee':
            return 'ph:coffee-bold'
        case 'podium':
        case 'person':
            return 'fluent:presenter-24-regular'
        case 'briefcase':
            return 'ph:briefcase-duotone'
        case 'certificate':
            return 'tabler:certificate'
        case 'gift':
            return 'mdi:gift-outline'
        case 'room':
            return 'fluent:conference-room-24-regular'
        case 'cash':
            return 'mdi:currency-thai-baht'
        case 'budget':
            return 'arcticons:expense-register'
        case 'accessories':
            return 'ph:note-pencil'
        case 'printer':
            return 'mdi:printer'
        case 'mobile':
            return 'material-symbols:mobile-screen-share'
        case 'books':
            return 'raphael:books'
        case 'hotel':
            return 'material-symbols:king-bed-outline'
        case 'airplane':
            return 'mdi:airplane-plus'
        case 'taxi':
            return 'cil:taxi'
        case 'car':
            return 'material-symbols:directions-car-outline-rounded'
        case 'transport':
            return 'material-symbols:emoji-transportation-sharp'
        case 'train':
            return 'ic:outline-train'
        case 'note':
            return 'material-symbols:edit-note'
        case 'stage':
            return 'fluent-emoji-high-contrast:pine-decoration'
        case 'included':
            return 'material-symbols:check-small-rounded';
        default:
            return 'material-symbols:new-label-rounded';
    }
}

